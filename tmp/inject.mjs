import { readFileSync, writeFileSync } from "node:fs";
const IDX = "/Users/deveshlakwal/revenue-os/.claude/worktrees/task-26/apps/www/index.html";
const CSS = "/private/tmp/claude-501/-Users-deveshlakwal-revenue-os-orchestrator/1a7a1722-1e1c-40b5-8149-97a09b3a6d51/scratchpad/fontface.css";
const sentinel = "/* __FONTFACE__ */";
let html = readFileSync(IDX, "utf8");
if (!html.includes(sentinel)) throw new Error("sentinel not found");
if (html.split(sentinel).length !== 2) throw new Error("sentinel not unique");
const css = readFileSync(CSS, "utf8").trimEnd();
html = html.replace(sentinel, css);
writeFileSync(IDX, html);
